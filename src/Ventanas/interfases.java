    private void selDispositivoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_selDispositivoItemStateChanged
      String dispositivo= selDispositivo.getSelectedItem().toString();
        if(dispositivo.equals("*Iphone 15*")){
          precio.setText("999");
      }
     
      if(dispositivo.equals("*Airpods*")){
          precio.setText("89.99");
      }
      
      if(dispositivo.equals("*Apple wacth*")){
          precio.setText("449.00");
      }
      
      if(dispositivo.equals("*Ipad Mini*")){
          precio.setText("499.00");
      }
    }//GEN-LAST:event_selDispositivoItemStateChanged

