    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        try {
        String Nombre=nom.getText().trim();
        String apellido=ape.getText().trim();
        String dispositivo=selDispositivo.getSelectedItem().toString();
        String metodoPago = selMetodoPago.getSelectedItem().toString();
        String cedula=id.getText().trim();
        double costo=Double.parseDouble(precio.getText( ));
        
        if(!Nombre.equals("")&& metodoPago !=null && !metodoPago.isEmpty() && !apellido.equals("")&& !cedula.equals("")){
         Limpiar();
         JOptionPane.showMessageDialog(null,"La compra del producto "+dispositivo+" a sido exitosa por el usuario "+Nombre+" "+apellido+" cuya cedula es "+cedula+" por "+costo);
        }else{
            JOptionPane.showMessageDialog(this, "Complete todos los campos");
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se ah selecionado un producto");
            System.out.println(""+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

